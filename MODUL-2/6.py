def Manusia (object):
    def __init__(self, nama, umur, alamat):
        self.nama = nama
        self.umur = umur
        self.alamat = alamat

    def ambilNama(self, nama):
        return self.nama

    def ambilUmur(self, umur):
        return self.umur

    def ambilAlamat(self, nama):
        return self.alamat

def siswaSMA(Manusia):
    def __init__(self, nama, noAbsen, kelas, jurusan):
        self.nama = nama
        self.noAbsen = absen
        self.kelas = kelas
        self.jurusan = jurusan

    def ambilNama(self, nama):
        return self.nama

    def ambilNoAbsen(self, noAbsen):
        return self.noAbsen

    def ambilKelas(self, kelas):
        return self.kelas
    
    def ambilJurusan(self, jurusan):
        return self.jurusan
